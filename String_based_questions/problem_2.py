'''String Built in functions'''

'''Convert a string to uppercase.'''

a='apple'
print(a.upper()) # converting to upper case

b='BANANA'
print(b.lower()) # converting to lower case

print(a.capitalize()) # converting first character to capital


'''Convert every word into title case.'''

sentence='I have completed the btech in the stream of AIML'

print(sentence.title()) # it will convert all the first letter into capital remaining small

